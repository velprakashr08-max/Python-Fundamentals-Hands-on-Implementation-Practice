def say_hi():
    print("Hello World")
print("Top")
say_hi()
print("Bottom")

def say_hello(name):
    print("Hello " + name)
say_hello("Jack")

def say_hey(name, age):
    print("Hello " + name + ", you are " + age)
say_hey("Jeff", "25")

def say_bye(name, age):
    print("Hello " + name + ", you are " + str(age))
say_bye("Jim", 20)